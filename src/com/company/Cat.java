package com.company;

public class Cat {
    private String name;
    private int age;
    private String color;

    //Setter
    public void setName (String name1){
        name=name1;
    }
    //Getter
    public String getName(){
        return name;
    }
    public void setAge (int years){
        age=years;
    }
    public int getAge(){
        return age;
    }
    public void setColor (String color1){
        color=color1;
    }
    public String getColor(){
        return color;
    }
}
